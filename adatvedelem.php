<h2>Táltos Autósiskola 2018 Adatvédelmi Tájékoztatója</h2>

<h3>Tisztelt Tanulóink!</h3>

<p>Jelen ismertetőnkben Tájékoztatjuk önöket arról, hogy 2018 05.25- től az Európai Parlament és a Tanács (EU) 2016/679 rendelete a természetes személynek a személyes adatok kezelése tekintetében történő védelméről és az ilyen szabad áramlásáról, valamint a 95/46/EK rendelet hatályon kívüli helyezéséről (továbbiakban Általános Adatvédelmi Rendelet vagy GDPR) az adatvédelmi kérdések szabályozása tekintetében Európa szerte és minden szektorban felváltja a hatályos jogszabályokat. Ennek következtében Magyarországon adatvédelmi kérdésekben már nem az információs önrendelkezési jogról és az információszabadságról szóló 2011.évi CXII. törvény az irányadó, hanem a GDPR. Emiatt módosul a tanulói szerződés és tanulói tájékoztató  Adatvédelmi Előírásai.</p>

<p>A GDPR legfontosabb célja, hogy egységesítse a korábban eltérő adatvédelmi tagállami szabályokat, megteremtve ezzel a lehetőséget arra, hogy európai szinten egységes adatkezelési szabályrendszer kialakításának, mégpedig általánosan, minden ipari és szolgáltatási szektorban. A jogalkotó legfontosabb célkitűzése e körben, hogy biztosítsa az adatkezeléssel érintettek jogainak magas szintű és hatékony védelmét, valamint, hogy az adatkezelői adatfeldolgozói tevékenységet ellátó szervezeteket ennek megfelelő és átlátható (adatvédelmi) működésre ösztönözze.</p>

<p>Mivel Autósiskolám kölcsönös bizalmon alapuló és jó kapcsolat kialakítására törekszik a tanulói jogok biztosítására és a jogszabályi megfelelésre, ezért átláthatóvá teszi a tanulók adatainak kezelését érintő tevékenységet és elérhetővé teszi a szabályrendszerét.</p>

